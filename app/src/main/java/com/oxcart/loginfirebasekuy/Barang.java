package com.oxcart.loginfirebasekuy;

public class Barang {
    private String namaBarang;
    private String jenisBarang;
    private String umurBarang;
    private int jumlahPerKilo;
    private int hargaBarang;

    public Barang() {
    }

    public String getNamaBarang() {
        return namaBarang;
    }

    public void setNamaBarang(String namaBarang) {
        this.namaBarang = namaBarang;
    }

    public String getJenisBarang() {
        return jenisBarang;
    }

    public void setJenisBarang(String jenisBarang) {
        this.jenisBarang = jenisBarang;
    }

    public String getUmurBarang() {
        return umurBarang;
    }

    public void setUmurBarang(String umurBarang) {
        this.umurBarang = umurBarang;
    }

    public int getJumlahPerKilo() {
        return jumlahPerKilo;
    }

    public void setJumlahPerKilo(int jumlahPerKilo) {
        this.jumlahPerKilo = jumlahPerKilo;
    }

    public int getHargaBarang() {
        return hargaBarang;
    }

    public void setHargaBarang(int hargaBarang) {
        this.hargaBarang = hargaBarang;
    }
}
